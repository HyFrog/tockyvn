export function convertTocky(word) {
  var dau = { character: "", tocky: "" }
  var chinh = { character: "", tocky: "" }
  var cuoi = { character: "", tocky: "" }
  for (let index = 0; index < firstSyllable.length; index++) {
    let character = firstSyllable[index].character
    let position = word.indexOf(character)
    if (position !== -1) {
      dau = firstSyllable[index];
      word = word.slice(character.length)
      break;
    }
  }

  for (let index = 0; index < mainSyllable.length; index++) {
    let character = mainSyllable[index].character
    let position = word.indexOf(character)
    if (position !== -1) {
      chinh = mainSyllable[index];
      word = word.slice(character.length)
      break;
    }
  }

  for (let index = 0; index < lastSyllable.length; index++) {
    let character = lastSyllable[index].character
    let position = word.indexOf(character)
    if (position !== -1) {
      cuoi = lastSyllable[index];
      word = word.slice(character.length)
      break;
    }
  }

  return dau.tocky + chinh.tocky + cuoi.tocky;
}

export function converInputTocky(word) {

  word = word.toUpperCase()

  let dau = "", chinh = "", cuoi = "";

  firstKeyboard.forEach(function (e) {
    if (word.includes(e.character)) {
      dau += (e.tocky);
      word.replace(e.character, '')
    }
  })

  lastKeyboard.forEach(function (e) {
    if (word.includes(e.character)) {
      cuoi += (e.tocky);
      word.replace(e.character, '')
    }
  })
  if (cuoi.length > 0) cuoi = '-' + cuoi;

  banphimthanh.forEach(function (e) {
    if (word.includes(e.character)) {
      chinh += (e.tocky);
      word.replace(e.character, '')
    }
  })

  mainKeyboard.forEach(function (e) {
    if (word.includes(e.character)) {
      chinh += (e.tocky);
      word.replace(e.character, '')
    }
  })

  if (chinh.length > 0) {
    chinh = `-${chinh}`;
  }

  let firstSyllableTocky = [...firstSyllable].sort((a, b) => (b.tocky.length - a.tocky.length));
  let mainSyllableTocky = [...mainSyllable].sort((a, b) => (b.tocky.length - a.tocky.length));
  let lastSyllableTocky = [...lastSyllable].sort((a, b) => (b.tocky.length - a.tocky.length));

  let textDau = "", textChinh = "", textCuoi = "";

  for (let index = 0; index < firstSyllableTocky.length; index++) {
    let tocky = firstSyllableTocky[index].tocky
    let position = dau.indexOf(tocky)
    if (position !== -1) {
      textDau = firstSyllableTocky[index].character;
      break;
    }
  }

  for (let index = 0; index < mainSyllableTocky.length; index++) {
    let tocky = mainSyllableTocky[index].tocky
    let position = chinh.indexOf(tocky)
    if (position !== -1) {
      textChinh = mainSyllableTocky[index].character;
      break;
    }
  }

  for (let index = 0; index < lastSyllableTocky.length; index++) {
    let tocky = lastSyllableTocky[index].tocky
    let position = cuoi.indexOf(tocky)
    if (position !== -1) {
      textCuoi = lastSyllableTocky[index].character;
      break;
    }
  }

  if (dau.length > 0 && textDau.length === 0) return false;
  if (chinh.length > 0 && textChinh.length === 0) return false;
  if (cuoi.length > 0 && textCuoi.length === 0) return false;

  return textDau + textChinh + textCuoi;
}

